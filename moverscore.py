import os
def setup():
    os.system('pip install transformers')
    os.system('pip3 install moverscore')

def get_score():
    from moverscore_v2 import get_idf_dict, word_mover_score 
    from collections import defaultdict
    references=input('Enter the refrence sentence : ')
    translations=input('Enter the translation or the test sentence : ')
    order = int(input('Enter the order of bigram : '))
    idf_dict_hyp = get_idf_dict(translations) # idf_dict_hyp = defaultdict(lambda: 1.)
    idf_dict_ref = get_idf_dict(references) # idf_dict_ref = defaultdict(lambda: 1.)

    scores = word_mover_score([references], [translations], idf_dict_ref, idf_dict_hyp, \
                            stop_words=[], n_gram=order, remove_subwords=True)
    return scores


if __name__=="__main__":
    setup()
    score = get_score()
    print(score)







