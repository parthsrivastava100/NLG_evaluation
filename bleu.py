import os
os.system('pip install nltk')

def bleu_score():
  import nltk
  print(' The different sentences should be terminated by a full.')
  ref_sent=list(input(' Enter the list of refrence sentences : ').split('.'))
  hyp_sent=list(input(' Enter the list of hypothesis sentences : ').split('.'))
  n=int(input('Enter the value of n (for n-gram overlap) : '))
  weighs=(1/n,)*n
  ref_sent_list=[item.split(' ') for item in ref_sent[0:len(ref_sent)-1]]
  hyp_sent_list=[item.split(' ') for item in hyp_sent[0:len(hyp_sent)-1]]
  ref_merged=[]
  hyp_merged=[]
  for l in ref_sent_list:
    ref_merged+=l
  for l in hyp_sent_list:
    hyp_merged+=l
  ref_sent = [ref_merged]
  l_ref=[ref_sent]
  l_hyp=[hyp_merged]
  corp_score= nltk.translate.bleu_score.corpus_bleu(l_ref, l_hyp,weights=weighs)
  print('The 1-gram BLEU score is {}'.format(corp_score))

if __name__=='__main__':
  bleu_score()