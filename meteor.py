import os
os.system('pip install nltk')
import nltk
nltk.download('wordnet')

def meteor_score():
  score=0
  print(' The different sentences should be terminated by a full stop.')
  ref_sent=list(input(' Enter the list of refrence sentences : ').split('.'))
  hyp_sent=list(input(' Enter the list of hypothesis sentences : ').split('.'))
  ref_sent_list=[item for item in ref_sent[0:len(ref_sent)-1]]
  hyp_sent_list=[item for item in hyp_sent[0:len(hyp_sent)-1]]
  for index,hyp in enumerate(hyp_sent_list):
    corp_score= nltk.translate.meteor_score.meteor_score(ref_sent, hyp)
    print('The Meteor score for line {} hypothesis is {}'.format(index+1,corp_score))
    score += corp_score
  print(' The Meteor score for the entire hypothesis is {}'.format(score/len(hyp_sent_list)))


if __name__=='__main__':
  meteor_score()


















